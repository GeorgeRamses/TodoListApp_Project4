@ExperimentalCoroutinesApi
@RunWith(AndroidJUnit4::class)
//Unit test the DAO
@SmallTest
class RemindersDaoTest {

    @get:Rule
    var instantExecutorRule = InstantTaskExecutorRule()

    private lateinit var database: RemindersDao

    @Before
    fun initDb() {
        // Using an in-memory database so that the information stored here disappears when the
        // process is killed.
        database = Room.inMemoryDatabaseBuilder(
            getApplicationContext(),
            RemindersDao::class.java
        ).build()


//        @After
//        fun closeDb() = database.close()
    }

    @Test
    suspend fun insertAndGet() {
        val reminder = ReminderDTO("Test", "Test_Desc", "Test_location", 0.0, 0.0)
        database.saveReminder(reminder)

        val loaded = database.getReminderById(reminder.id)
        assertThat(loaded as ReminderDTO, notNullValue())
        assertThat(loaded.id, `is`(reminder.id))
        assertThat(loaded.title, `is`(reminder.title))
        assertThat(loaded.description, `is`(reminder.description))
    }
}


